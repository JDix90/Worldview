/** "UAL123" → "United Airlines" (null when the prefix isn't a known airline). */
export function airlineFromCallsign(callsign: string | undefined): string | null {
  if (!callsign) return null;
  const prefix = callsign.trim().slice(0, 3).toUpperCase();
  return AIRLINES[prefix] ?? null;
}
